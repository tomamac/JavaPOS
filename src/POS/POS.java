    private void loginokBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginokBtnActionPerformed
        if(verifier(usernameTxt.getText(),String.valueOf(passTxt.getPassword())) == 1){
            loginPrompt.dispose();
            loginBtn.setEnabled(false);
            loginerrorLabel.setVisible(false);
            homePage.setEnabled(true);
            String name = usernameTxt.getText().equals("admin") ? "ADMIN" : "EMPLOYEE";
            welcomeLabel.setText("WELCOME BACK " + name);
            usernameTxt.setText("");
            passTxt.setText("");
            mainBtn.setEnabled(true);
            stockBtn.setEnabled(true);
            editBtn.setEnabled(name.equals("ADMIN") ? true:false);
            logoutBtn.setEnabled(true);
            setCount(0);
        }
        else{
            loginerrorLabel.setVisible(true);
            loginerrorLabel.setText("There was a problem with your username or password. Please try again." + " (" + (getCount()+1) + ")");
            if(getCount() == 2){
                try {
                    TimeUnit.SECONDS.sleep(5);
                    quit();
                } catch (InterruptedException ex) {
                    Logger.getLogger(POS.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            countUp(1);
        }
    }//GEN-LAST:event_loginokBtnActionPerformed

