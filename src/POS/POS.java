        public int verifier(String username, String pass){
            int verifyUser = 0;
            int verifyPass = 0;
            for (String username_ : getUsername()) {
                if(!username.equals(username_)){
                    verifyUser = 0;
                }
                else{
                    verifyUser = 1;
                    break;
                }
            }
            
            for (String password_ : getPass()){
                if(!pass.equals(password_)){
                    verifyPass = 0;
                }
                else{
                    verifyPass = 1;
                    break;
                }
            }
            int verify = verifyUser == 1 && verifyPass ==1 ? 1 : 0;
            return verify;
        }
